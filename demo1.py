# Script to calculate Simple Interest using the formula
# si = (p * t * r)/100
#

principal = 45000
t = 20
rate_of_interest = 0.07 # Here rate of interest is 7%

simple_interest = (principal * t * rate_of_interest)/100
print('Simple Interest is: ')
print(simple_interest)
